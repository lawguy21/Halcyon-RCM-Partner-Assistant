// Admin-only nav items
const adminNavItems: NavItem[] = [
  {
    name: 'Admin',
    href: '/admin',
    icon: (
      <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
        <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 6V4m0 2a2 2 0 100 4m0-4a2 2 0 110 4m-6 8a2 2 0 100-4m0 4a2 2 0 110-4m0 4v2m0-6V4m6 6v10m6-2a2 2 0 100-4m0 4a2 2 0 110-4m0 4v2m0-6V4" />
      </svg>
    ),
    requiredRole: 'ADMIN',
  },
];

export default function Navigation() {
  const pathname = usePathname();
  const { user, isAuthenticated, isLoading } = useAuth();

  const isActive = (href: string) => {
    if (href === '/') {
      return pathname === '/';
    }
    return pathname.startsWith(href);
  };

  // Filter nav items based on user role
  const visibleNavItems = navItems.filter((item) => {
    if (!item.requiredRole) return true;
    return hasRole(user, item.requiredRole);
  });

  // Add admin items if user is admin
  const allNavItems = user?.role === 'ADMIN'
    ? [...visibleNavItems, ...adminNavItems]
    : visibleNavItems;

  const getInitials = (name: string) => {
    return name
      .split(' ')
      .map((n) => n[0])
      .join('')
      .toUpperCase()
      .slice(0, 2);
  };

  return (
    <nav className="w-64 bg-slate-900 min-h-screen flex flex-col">
      {/* Logo */}
      <div className="p-6 border-b border-slate-800">
        <Link href="/" className="flex items-center space-x-3">
          <div className="w-10 h-10 bg-blue-600 rounded-lg flex items-center justify-center">
            <svg className="w-6 h-6 text-white" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M13 10V3L4 14h7v7l9-11h-7z" />
            </svg>
          </div>
          <div>
            <h1 className="text-white font-semibold text-lg leading-tight">Halcyon</h1>
            <p className="text-slate-400 text-xs">RCM Partner Assistant</p>
          </div>
        </Link>
      </div>

      {/* Navigation Items */}
      <div className="flex-1 py-6">
        <ul className="space-y-1 px-3">
          {allNavItems.map((item) => (
            <li key={item.href}>
              <Link
                href={item.href}
                className={`flex items-center space-x-3 px-4 py-3 rounded-lg transition-colors ${
                  isActive(item.href)
                    ? 'bg-blue-600 text-white'
                    : 'text-slate-300 hover:bg-slate-800 hover:text-white'
                }`}
              >
                {item.icon}
                <span className="font-medium">{item.name}</span>
              </Link>
            </li>
          ))}
        </ul>
      </div>

      {/* Quick Action Button - Only show for users who can create assessments */}
      {isAuthenticated && hasRole(user, 'USER') && (
        <div className="p-4 border-t border-slate-800">
          <Link
            href="/assessments/new"
            className="flex items-center justify-center space-x-2 w-full px-4 py-3 bg-blue-600 hover:bg-blue-700 text-white rounded-lg transition-colors font-medium"
          >
            <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 4v16m8-8H4" />
            </svg>
            <span>New Assessment</span>
          </Link>
        </div>
      )}

      {/* User Section */}
      <div className="p-4 border-t border-slate-800">
        {isLoading ? (
          <div className="flex items-center space-x-3 px-2">
            <div className="w-8 h-8 bg-slate-700 rounded-full animate-pulse"></div>
            <div className="flex-1">
              <div className="w-20 h-3 bg-slate-700 rounded animate-pulse mb-1"></div>
              <div className="w-16 h-2 bg-slate-700 rounded animate-pulse"></div>
            </div>
          </div>
        ) : isAuthenticated && user ? (
          <Link
            href="/profile"
            className="flex items-center space-x-3 px-2 py-2 rounded-lg hover:bg-slate-800 transition-colors"
          >
            <div className="w-8 h-8 bg-blue-600 rounded-full flex items-center justify-center text-white text-sm font-medium">
              {getInitials(user.name)}
            </div>
            <div className="flex-1 min-w-0">
              <p className="text-sm font-medium text-white truncate">{user.name}</p>
              <p className="text-xs text-slate-400 truncate">{user.email}</p>
            </div>
            <svg className="w-4 h-4 text-slate-400" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 5l7 7-7 7" />
            </svg>
          </Link>
        ) : (
          <Link
            href="/auth/login"
            className="flex items-center justify-center space-x-2 w-full px-4 py-3 bg-slate-800 hover:bg-slate-700 text-white rounded-lg transition-colors font-medium"
          >
            <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M11 16l-4-4m0 0l4-4m-4 4h14m-5 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h7a3 3 0 013 3v1" />
            </svg>
            <span>Sign In</span>
          </Link>
        )}
      </div>

      {/* Footer */}
      <div className="p-4 text-center">
        <p className="text-slate-500 text-xs">Halcyon Recovery Systems</p>
      </div>
    </nav>
  );
}
